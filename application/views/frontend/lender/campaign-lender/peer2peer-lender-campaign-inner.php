	document.getElementById('lender_submit_payment_button').onclick = function (e) {
		var amount = ($("#funding_amount").val() * 100); // Amount In Paise
		var name = $("#name").val();
		var email = $("#email").val();
		var mobile = $("#mobile").val();
		var address = $("#address").val();
		var is_validate = true;
		if ($("#funding_amount").val() == "") {
			alert("Please enter amount");
			is_validate = false;
			return false;
		}
		if (name == "") {
			alert("Please enter name");
			is_validate = false;
			return false;
		}
		if (email == "") {
			alert("Please enter email");
			is_validate = false;
			return false;
		}
		if (mobile == "") {
			alert("Please enter mobile");
			is_validate = false;
			return false;
		}
		if (address == "") {
			alert("Please enter address");
			is_validate = false;
			return false;
		}
		if (is_validate == true) {
			$.ajax({
				type: "POST",
				url: "<?php echo base_url(); ?>lenderaction/saveLending",
				data: {amount: amount, name: name, email: email, mobile: mobile, address: address},
				datatype: 'json',
				success: function (data) {
					var response = JSON.parse(data);
					var last_insert_id = response.last_insert_id;
					var options = {
						"key": "<?=$razorpay_public_key?>",
						"amount": amount,
						"name": "Antworks P2P Financial PVT. LTD.",
						"description": "Funding Amount",
						"image": "https://antworksp2p.com/assets/img/logo.png",
						"handler": function (response) {
							if (response.razorpay_payment_id != '') {
								$.ajax({
									type: "POST",
									url: "<?php echo base_url(); ?>lenderaction/updateafterPayment",
									data: {
										razorpay_payment_id: response.razorpay_payment_id,
										amount: amount,
										last_insert_id: last_insert_id
									},
									datatype: 'json',
									success: function (data) {
										var res = JSON.parse(data);
										if (res.status == 1) {
											alert("Thanks for choosing Antworks P2P Financing PVT. LTD. ");
                                            window.location.href = "<?=base_url()?>home/lender_lending_campaign_thank_you";
										}
									}
								});
							} else {
								alert("Please Make Payment First");
							}
						},
						"prefill": {
							"name": name,
							"email": email,
							"contact": mobile,
						},
						"theme": {
							"color": "#00518c"
						}
					};
					var rzp1 = new Razorpay(options);
					rzp1.open();
				}
			});
		}

		e.preventDefault();
	}

</script>
